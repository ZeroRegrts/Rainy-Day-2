const CHARS_PER_LINE = 12;
const LINES_SHOWN = 3;

function getWindowStart() {
  const cursorLine = Math.floor((cursorIndex - startOffset) / CHARS_PER_LINE);
  /* scroll only when cursor reaches line 2 (index 1), keeping it on line 1 */
  if (cursorLine >= windowLine + 2) {
    windowLine = cursorLine - 1;
  }
  return startOffset + windowLine * CHARS_PER_LINE;
}

function renderWindow() {
  charDisplay.innerHTML = '';
  const winStart = getWindowStart();

  for (let line = 0; line < LINES_SHOWN; line++) {
    const lineDiv = document.createElement('div');
    lineDiv.className = 'type-line';

    for (let col = 0; col < CHARS_PER_LINE; col++) {
      const absIdx = ((winStart + line * CHARS_PER_LINE + col) % chars.length + chars.length) % chars.length;

      if (absIdx === cursorIndex && started) {
        lineDiv.appendChild(cursor);
      }

      const span = document.createElement('span');
      span.className = 'char-zh';
      span.dataset.abs = absIdx;
      span.textContent = chars[absIdx];

      const state = typedBuffer[absIdx];
      if (state === 'correct') span.classList.add('correct');
      else if (state === 'incorrect') span.classList.add('incorrect');

      lineDiv.appendChild(span);
    }

    charDisplay.appendChild(lineDiv);
  }

  if (started) updateInputPosition();
}

function updateInputPosition() {
  const rect = cursor.getBoundingClientRect();
  hiddenInput.style.top = (rect.bottom + 40) + 'px';
  hiddenInput.style.left = rect.left + 'px';
}

