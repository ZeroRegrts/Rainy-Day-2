const RECENT_LIMIT = 10;

function initPreview() {
  windowLine = 0;
  const sentenceEnds = [];
  chars.forEach((ch, i) => {
    if ('。！？'.includes(ch)) sentenceEnds.push(i + 1);
  });
  const validStarts = sentenceEnds.filter(i => i < chars.length);

  /* exclude recently used starts */
  const available = validStarts.filter(s => !recentStarts.includes(s));
  const pool = available.length > 0 ? available : validStarts;

  const randomPos = Math.floor(Math.random() * chars.length);
  let closest = pool[0];
  let minDist = Math.abs(randomPos - pool[0]);
  for (let i = 1; i < pool.length; i++) {
    const dist = Math.abs(randomPos - pool[i]);
    if (dist < minDist) {
      minDist = dist;
      closest = pool[i];
    }
  }
  startOffset = closest;

  recentStarts.push(startOffset);
  if (recentStarts.length > RECENT_LIMIT) recentStarts.shift();

  cursorIndex = startOffset;
  typedBuffer = [];
  renderWindow();
  focusInput();
}

/* ─────────────────────────────────────────────
   15. INIT TEST
───────────────────────────────────────────── */
function initTest() {
  lockControls();
  practiceSeconds = 0;
  lastSampleChars = 0;
  started = true;
  correctChars = 0;
  totalChars = 0;

  enterFocusMode();
  mouseIdleTimeout = setTimeout(() => {
    if (started) enterFocusMode();
  }, 3000);

  if (mode === 'speedtest') {
    startTimer();
    startWpmSampling();
  } else {
    timerEl.textContent = '∞';
    focusTimerEl.textContent = '∞';
    timerInterval = setInterval(() => {
      practiceSeconds++;
    }, 1000);
  }
}

/* ─────────────────────────────────────────────
   16. END TEST
───────────────────────────────────────────── */
function endTest() {
  clearInterval(timerInterval);
  clearInterval(wpmSampleInterval);
  clearTimeout(mouseIdleTimeout);
  unlockControls();
  started = false;
  hiddenInput.disabled = true;
  visibleInput.disabled = true;
  exitFocusMode();

  const duration = getTimerDuration();
  const totalSeconds = mode === 'speedtest' ? duration : practiceSeconds;
  const wpm = Math.round((correctChars / 2) / Math.max(totalSeconds / 60, 1 / 60));
  const acc = totalChars > 0
    ? Math.round((correctChars / totalChars) * 100)
    : 100;

  document.getElementById('result-wpm').textContent = wpm;
  document.getElementById('result-accuracy').textContent = acc + '%';
  document.getElementById('result-chars').textContent = correctChars;
  resultsScreen.classList.remove('hidden');
}

