function processChar(ch) {
  if (!started) initTest();
  if (paused) {
    paused = false;
    if (mode === 'speedtest') {
      resumeTimer();
    } else {
      resumePracticeTimer();
    }
    enterFocusMode();
  }

  const expected = chars[cursorIndex];
  const isCorrect = normalize(ch) === normalize(expected);

  if (!isCorrect && isStrictMode()) {
    const blocks = charDisplay.querySelectorAll('.char-zh');
    const activeBlock = [...blocks].find(b => parseInt(b.dataset.abs) === cursorIndex);
    if (activeBlock) {
      activeBlock.classList.remove('shake');
      void activeBlock.offsetWidth;
      activeBlock.classList.add('shake');
      setTimeout(() => activeBlock.classList.remove('shake'), 300);
    }
    if (showInputBox) visibleInput.value = '';
    return;
  }

  const prevState = typedBuffer[cursorIndex];
  typedBuffer[cursorIndex] = isCorrect ? 'correct' : 'incorrect';

  if (prevState === undefined) {
    totalChars++;
    if (isCorrect) correctChars++;
  } else {
    if (prevState === 'correct' && !isCorrect) correctChars--;
    if (prevState === 'incorrect' && isCorrect) correctChars++;
  }

  cursorIndex = (cursorIndex + 1) % chars.length;
  updateStats();
  renderWindow();

  if (showInputBox) visibleInput.value = '';
}

function processBackspace() {
  if (!started) return;
  if (paused) {
    paused = false;
    if (mode === 'speedtest') {
      resumeTimer();
    } else {
      resumePracticeTimer();
    }
    enterFocusMode();
  }
  if (cursorIndex === startOffset) return;
  resetBlink();

  cursorIndex = (cursorIndex - 1 + chars.length) % chars.length;
  const prevState = typedBuffer[cursorIndex];
  typedBuffer[cursorIndex] = undefined;

  if (prevState !== undefined) {
    if (totalChars > 0) totalChars--;
    if (prevState === 'correct' && correctChars > 0) correctChars--;
  }

  renderWindow();
  updateStats();
}

function attachInputListeners(input) {
  input.addEventListener('compositionstart', () => { isComposing = true; });

  input.addEventListener('compositionend', () => {
    isComposing = false;
    setTimeout(() => {
      const val = input.value;
      input.value = '';
      for (const ch of val) processChar(ch);
    }, 100);
  });

  input.addEventListener('input', () => {
    if (isComposing) return;
    const val = input.value;
    if (!val) return;
    const lastChar = val[val.length - 1];
    const isChinese = /[\u4e00-\u9fff\u3400-\u4dbf]/.test(lastChar);
    if (isChinese) {
      input.value = '';
      for (const ch of val) processChar(ch);
    }
  });

  input.addEventListener('keydown', (e) => {
    if (e.key === 'Backspace') {
      e.preventDefault();
      processBackspace();
      return;
    }
    if (isComposing) return;
    const val = input.value;
    if (val && !/[\u4e00-\u9fff\u3400-\u4dbf]/.test(val[val.length - 1])) {
      for (const ch of val) processChar(ch);
      input.value = '';
    }
  });
}

